package Chapter5Labs.QuadrantFinder;

public class Point
{
    private double x;
    private double y;

    public Point(double xx, double yy)
    {
        x = xx;
        y = yy;
    }

    public double getX()
    {
        return x;
    }

    public double getY()
    {
        return y;
    }
}
